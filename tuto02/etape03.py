# Type booléen :
# valeurs True ou False
# Exemple :
actif = True
if actif:
    print("La prise de courant est activée")
else:
    print("La prise de courant est désactivée")


# Types Numériques :
# Entiers (int): Représentent des nombres entiers.
# Décimaux (float): Représentent des nombres à virgule flottante.
# Exemple :
nombre_entier = 42
nombre_decimal = 3.14


# Chaînes de Caractères :
# Les chaînes de caractères sont définies avec des guillemets simples ou doubles.
# Exemple :
nom = "Ingénieur"
specialite = 'Génie Civil'
message = f"Bonjour, je suis {nom}, spécialisé en {specialite}."
print(message)

# Listes et Tuples :
# Listes (list): Séquence mutable d'éléments.
# Tuples (tuple): Séquence immuable d'éléments.
# Exemple :
materiaux = ['béton', 'acier', 'bois']
dimensions = (10, 5, 3)

# Dictionnaires :
# Les dictionnaires stockent des paires clé-valeur.
# Exemple :
propriétés = {'matériau': 'béton', 'résistance': 30, 'unité': 'MPa'}
